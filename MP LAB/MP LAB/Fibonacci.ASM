ASSUME CS:CODE, DS:DATA
DATA SEGMENT
OUT1 DB "ENTER RANGE :$"
OUT2 DB 13,10,"FIBONACCI SERIES :$"
A DW 0000H
B DW 0001H
C DW 0000H
N DW ?
SPACE DB ' $'

DATA ENDS
CODE SEGMENT
START:	MOV AX,DATA
        MOV DS,AX
        MOV DX,OFFSET OUT1
	MOV AH,9
	INT 21H
	MOV N,0

INPUT:	MOV AH,1
	INT 21H
	CMP AL,13
	JZ SET
	SUB AL,30H
	MOV AH,0
	MOV CX,AX
	MOV AX,N
	MUL BL
	ADD AX,CX
	MOV N,AX
	JMP INPUT

SET:	MOV SI,N
	MOV AX,C
	MOV DI,B

MSG:	LEA DX,OUT2
	MOV AH,9
	INT 21H
	MOV AX,0

L1:	CALL DISP
	LEA DX,SPACE
        MOV AH,09H
        INT 21H
        MOV AX,A
        ADD AX,DI
        MOV DI,A
        MOV A,AX
        MOV C,AX
        DEC SI
        JNZ L1
        MOV AH,4CH
        INT 21H

DISP PROC
     MOV CX,0000H
     MOV BX,000AH
L2:  MOV DX,0000H
     DIV BX
     ADD DX,0030H
     PUSH DX
     INC CX
     CMP AX,0000H
     JNZ L2
L3:  POP DX
     MOV AH,02H
     INT 21H
     LOOP L3
     RET
DISP ENDP
CODE ENDS
     END START
